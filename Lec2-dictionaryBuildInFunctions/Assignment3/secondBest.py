"""The "Second Best" Finder (Built-ins), Write a script using built-in functions to:
Find the maximum score using `max()`.
Find the minimum score using `min()`.
"""

score = [90,78,82,97,68,80,79,86,75]

print("Maximum score = ",max(score))#gives the max value of the string
print("Minimum score = ",min(score))#gives the least value of the string